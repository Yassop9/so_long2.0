#include "../Libft/includes/libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	result;
	char	sign;

	sign = '-';
	if (n < 0)
	{
		ft_putchar_fd(sign, fd);
		if (n == -2147483648)
		{
			ft_putchar_fd('2', fd);
			n = -147483648;
		}
		n *= -1;
	}
	if (n >= 10)
		ft_putnbr_fd(n / 10, fd);
	result = (n % 10) + '0';
	ft_putchar_fd(result, fd);
}
