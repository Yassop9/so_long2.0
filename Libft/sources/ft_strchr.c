#include "../Libft/includes/libft.h"

char	*ft_strchr(const char *s, int c)
{
	if (c == 0)
		return ((char *)s + ft_strlen(s));
	while (*s)
	{
		if (c % 256 == *s++)
			return ((char *)s - 1);
	}
	return (NULL);
}
