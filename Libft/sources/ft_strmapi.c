#include "../Libft/includes/libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	len;
	char	*result;
	size_t	i;

	i = -1;
	if (!s || !f)
		return (NULL);
	len = ft_strlen(s);
	result = malloc((len + 1) * sizeof(char));
	if (!result)
		return (NULL);
	while (++i < len)
		result[i] = f((unsigned int)i, s[i]);
	result[len] = '\0';
	return (result);
}
