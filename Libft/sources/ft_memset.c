#include "../Libft/includes/libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	char		*ptr;
	size_t		i;

	ptr = (char *)s;
	i = -1;
	while (++i < n)
		ptr[i] = c;
	return (s);
}
